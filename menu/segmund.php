          <font face="Anson"><font size="4" color="black">
            
            A Segunda Guerra Mundial, iniciada em setembro de 1939, foi a maior catástrofe provocada pelo homem em toda a sua longa história.
            <br><br>
             Ela estendeu-se de 1939 até 1945, resultando na morte de 60 milhões a 70 milhões de pessoas, embora existam estatísticas que sugiram que a guerra provocou mais que 70 milhões de mortos. O conflito teve como estopim a invasão da Polônia pelos alemães em 1º de setembro de 1939.<br>
<br>
Envolveu setenta e duas nações e foi travada em todos os continentes, de forma direta ou indiretamente. O número de mortos superou os cinquenta milhões, havendo ainda uns vinte e oito milhões de mutilados.<br>
<br>
É difícil de calcular quantos outros milhões saíram do conflito vivos, mas completamente inutilizados devido aos traumatismos psíquicos a que foram submetidos (bombardeios aéreos, torturas, fome e medo permanente). Outra de suas características, talvez a mais brutal, foi a supressão da diferença entre aqueles que combatem no fronte e a população civil na retaguarda. Essa guerra foi total. Nenhum dos envolvidos selecionou seus objetivos militares excluindo os civis.<br>
<br>

<img src="./imagens/hitler.jpg" width="38%" class="exemplor">
<br><br>
<b>Soldados da Segunda Guerra</b><br>
<br>
Atacar a retaguarda do inimigo, suas cidades, suas indústrias, suas mulheres, crianças e velhos passou a fazer parte daquilo que os estrategistas eufemisticamente classificavam como "guerra psicológica" ou "guerra de desgaste". Naturalmente que a evolução da aviação e das armas autopropulsadas permitiu-lhes que a antiga separação entre linha de frente e retaguarda fosse suprimida.<br>
<br>
Se a Primeira Guerra Mundial provocou um custo de 208 bilhões de dólares, esta atingiu a impressionante cifra de 1 trilhão e 500 bilhões de dólares, quantia que, se investida no combate da miséria humana a teria suprimido da face da terra. Aproximadamente 110 milhões de homens e mulheres foram mobilizados, dos quais apenas 30% não sofreram morte ou ferimento.<br>
<br>
Como em nenhuma outra, o engenho humano foi mobilizado integralmente para criar instrumentos cada vez mais mortíferos, sendo empregados a bomba de fósforo, a napalm e finalmente a bomba política de genocídio em massa, construindo-se campos especiais para tal fim. Com disse o historiador R.A.C. Parker: "O conceito que a humanidade tinha de si mesmo, nunca voltará a ser o mesmo".<br>
<br>
Enfim a Liga das Nações, órgão instituído para manter a paz entre as nações, não conseguiu cumprir o seu papel, e esfacelou mediante a corrida militarista preparada pelas nações inconformadas pela hegemonia política e militar exercida pelos vencedores da Primeira Guerra Mundial. Sem possuir uma única razão, essa guerra foi consequência do exacerbado desenvolvimento industrial das nações europeias. De certa forma, levando em consideração suas especificidades, a Segunda Guerra parecia uma continuidade dos problemas da Primeira Guerra.<br>
<br>
Desta forma, a Segunda Guerra é considerada como uma verdadeira guerra mundial, sendo uma consequência de um conjunto de continuidades e questões mal resolvidas pelos tratados de paz estabelecidos após a Primeira Guerra Mundial. Os confrontos foram divididos entre duas grandes coalizões militares: os Aliados, liderados por Estados Unidos, Inglaterra, França e União Soviética; e o Eixo, composto pela Itália, Alemanha e Japão. Em consequência de suas maiores dimensões, os conflitos foram desenvolvidos na Europa, Norte da África e países do Oceano Pacífico.
<br><br>

A Segunda Guerra Mundial teve como grande causa o expansionismo e o militarismo da Alemanha Nazista. Essa postura da Alemanha refletia diretamente a ideologia dos nazistas, que haviam alcançado o poder da Alemanha em 1933. A ação dos nazistas resultava, em grande parte, da insatisfação de uma parte radicalizada da sociedade alemã com o desfecho da Primeira Guerra Mundial.<br>
<br>
Ao final da Primeira Guerra Mundial, consolidou-se fortemente na sociedade alemã uma ideia de que a derrota na guerra havia sido injusta. Somado a isso, havia também a grande humilhação que a Alemanha sofreu com o Tratado de Versalhes, acordo que pôs fim à Primeira Guera e que proibia a Alemanha de ter navios e aviões de guerra, limitou ao número de 100 mil os soldados de infantaria, obrigou a nação alemã a pagar uma indenização altíssima e a entregar suas colônias para aqueles que a derrotaram.<br>
<br>
Para piorar, na década de 1920, durante a República de Weimar, a Alemanha encarou uma crise econômica duríssima, que levou o país à falência. Essa crise foi agravada com a Crise de 1929, que, por sua vez, reforçou a crise da democracia liberal e fomentou movimentos autoritários e fascistas pela Europa. O fascismo italiano e o nazismo alemão são os grandes exemplos.<br>
<br>
Os nazistas ocuparam o poder da Alemanha em 1933, e Adolf Hitler, o líder do partido nazista, iniciou uma campanha de recuperação da Alemanha, de doutrinação da população e de perseguição às minorias. A Alemanha, ao recuperar a sua economia, partiu para o rearmamento – um desafio claro às determinações do Tratado de Versalhes. Franceses e ingleses nada fizeram, pois temiam que um desafio aos alemães poderia levar a Europa a uma nova guerra, experiência essa que queriam evitar ao máximo.<br>
<br>
À medida que a Alemanha fortaleceu-se militarmente, Hitler deu início ao seu expansionismo territorial. A ideia de Hitler era construir o lebensraum, o “espaço vital” que os nazistas tanto almejavam. Esse conceito consistia basicamente em formar um império para a Alemanha em territórios que historicamente haviam sido ocupados por germânicos. Esse era o Terceiro Reich, um império dedicado exclusivamente para os arianos (ideal de raça pura dos nazistas) e que sobreviveria à custa da exploração dos eslavos.<br>
<br>
O expansionismo germânico ocorreu em três momentos distintos. Inicialmente foi realizada a invasão e anexação da Áustria, evento conhecido como Anschluss e que ocorreu em 1938. Em 1939, os alemães manifestaram o interesse de invadir e anexar os Sudetos, região da Checoslováquia. Após negociações conduzidas por britânicos e franceses, os alemães tiveram autorização para anexar os Sudetos (acabaram anexando quase toda a Checoslováquia). Por fim, veio a Polônia. Esse país do Leste Europeu havia surgido ao final da Primeira Guerra Mundial em territórios que anteriormente pertenciam aos alemães e aos russos. A retórica de Hitler contra os poloneses endureceu-se em meados de 1939. A invasão da Polônia, no entanto, não seria aceita por ingleses e franceses. Ambos os países haviam exigido de Hitler, durante a Conferência de Munique, que suas ambições territoriais encerrassem-se na Checoslováquia.<br>
<br>
Hitler, no entanto, não esperava que ingleses e franceses fossem reagir aos seus movimentos. Em 1º de setembro ordenou a invasão da Polônia utilizando como justificativa um suposto ataque polonês na fronteira com a Alemanha (o ataque foi forjado pelos nazistas). Dois dias depois, britânicos e franceses responderam à agressão alemã contra a Polônia com uma declaração de guerra. Esse foi o início da Segunda Guerra Mundial.<br>
<br>

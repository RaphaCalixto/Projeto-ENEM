<br><Br>
<b> Combatentes: </b> <br>
A Segunda Guerra Mundial contou com o envolvimento de dezenas de países. Os participantes da Segunda Guerra Mundial podem ser agrupados em dois grupos.<br>
<br>
Aliados: Reino Unido, França, União Soviética e Estados Unidos eram os membros principais;<br>
<br>
Eixo: Alemanha, Itália e Japão eram os membros principais.<br>
<br>
Adolf Hitler e Benito Mussolini eram os líderes da Alemanha e Itália, respectivamente, ambas as nações pertenciam ao Eixo.<br>
Adolf Hitler e Benito Mussolini eram os líderes da Alemanha e Itália, respectivamente. Ambas as nações pertenciam ao Eixo.<br>
<br>
Naturalmente, ao longo da guerra, diversos outros países foram tomando partido e juntando-se a um dos dois lados que estavam na luta. Do lado dos Aliados, por exemplo, lutaram o Canadá, o Brasil, a Austrália, a China, a Holanda etc. No Eixo, atuaram nações como Hungria, Romênia, Croácia etc. É importante mencionar que em diversos locais que os nazistas pisaram houve colaboracionismo, mas também houve resistência.<br>
<br>
Um símbolo do colaboracionismo com os nazistas foi Vidkun Quisling, nazista da Noruega que organizou o plano de invasão de seu próprio país com os alemães. Símbolos de resistência contra os nazistas foram, por exemplo, os guerrilheiros (partisans) da Bielorrússia (conhecida atualmente como Belarus) que organizaram forças nas florestas de seu país e atuaram por anos sabotando os nazistas.<br>
<br>
A batalha final no cenário de guerra europeu foi travada em Berlim, capital alemã, onde foi organizada a resistência final dos nazistas em uma situação tão desesperadora que havia tropas compostas por velhos e crianças. O ataque a Berlim foi realizado apenas pelos soviéticos e, logo após as tropas do Exército Vermelho entrarem no Reichstag (Parlamento alemão), Hitler e sua esposa (Eva Braun) cometeram suicídio. O comando da Alemanha foi transmitido para Karl Dönitz, e os alemães renderam-se oficialmente no dia 8 de maio de 1945.<br>
<br>
No cenário asiático, a guerra teve fim oficialmente no dia 2 de setembro de 1945, quando os japoneses assinaram sua rendição incondicional aos americanos. A rendição japonesa foi resultado direto do lançamento das bombas atômicas sobre Hiroshima, em 6 de agosto, e Nagasaki, em 9 de agosto.<br>
<br>
Após a Segunda Guerra Mundial, o mundo passou por intensas e radicais transformações. Logo após a guerra já estava predefinido o cenário que caracterizaria o mundo pelas décadas seguintes: o da bipolarização do período da Guerra Fria. O Leste Europeu foi ocupado pelas tropas do Exército Vermelho, e toda essa região ficou sob a influência do comunismo soviético.<br>
<br>
As potências dos Aliados reuniram-se em 1945 e debateram a respeito das mudanças territoriais que aconteceriam no mapa europeu. Assim, a Alemanha, por exemplo, perdeu territórios para os soviéticos (a chamada Prússia Oriental passou a ser da União Soviética e atualmente é conhecida como Oblast de Kaliningrado e fica na atual Rússia). Vale mencionar também que a Alemanha foi ocupada por tropas britânicas, americanas, francesas e soviéticas.<br>
<br>

<img src="./imagens/diad.jpg" width="38%" class="exemplor">
<br><br>
Após a Segunda Guerra, foram criados tribunais que julgaram os crimes de guerra cometidos por alemães e japoneses. Pessoas que estiveram diretamente envolvidas com o Holocausto e com os massacres cometidos pelo Japão na Ásia foram julgadas no Tribunal Militar Internacional de Nuremberg e no Tribunal Internacional para o Extremo Oriente.<br>
<br>

Após o final da Segunda Guerra Mundial, foi criada a Organização das Nações Unidas, conhecida como ONU e responsável pela manutenção da paz entre as nações. A intenção de uma organização como a ONU é evitar que outro conflito como a Segunda Guerra Mundial aconteça.<br>
<br>
Por fim, uma consequência direta da bipolarização do mundo, com os soviéticos representando um modelo e os americanos representando outro, foi a criação de um plano de reconstrução da Europa Ocidental financiado pelos Estados Unidos: o Plano Marshall.

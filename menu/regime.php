              <div style="text-align:justify;justify-content:center; margin-left: 20px; margin-right: 20px;" align="center">
          <font face="Anson"><font size="4" color="black">Foi um período da historia brasileira em que as forças armadas estiveram a frente do governo brasileiro, entre 1 de abril de 1964 e 15 de março de 1985.<br>
A ideia de um golpe militar surgiu quando setores da sociedade, governantes e parte das forças armadas  temiam uma suposta ameaça comunista no Brasil liderada por ate então presidente João Goulart.<br>
Em março de 64 ocorreu uma grande manifestação em São Paulo que defendia a derrubada de João Goulart, a chamada  “marcha da família com Deus pela liberdade”.<br>
Em 31 de março de 1964, João Goulart é desposto por meio de um golpe militar, fazendo com que o general Humberto de Alencar Castelo Branco assumisse o governo brasileiro, com o apoio das camadas politicas e civis do país.<br><Br>
<img src="./imagens/mulh.jpg" width="38%" class="exemplor">
<br><br>
Em seus 21 anos, o Brasil foi governado por 5 presidentes militares, marcados por uma forte repressão, censura, torturas, autoritarismo, crescimento econômico e diversas outras medidas que mudariam o rumo do Brasil.<br>
<br>
Foram estes que presidiram o país de 1964 a 1985, eleitos de forma de indireta, ou seja, pelo congresso<br>
    <b>• Humberto de Alencar Castelo Branco (1964-1967):<br></b>
    • eleito pelo congresso em 15 de abril de 1964<br>
    • implantação do bipartidarismo: MDB e ARENA<br>
    • Determinava eleições indiretas para a presidência da República<br>
    • Fechamento do congresso<br>
    • Lei de imprensa <br>
    • AI-1 - dava ao governo militar o poder de alterar a constituição, cassar leis legislativas, suspender direitos políticos por dez anos e demitir, colocar em disponibilidade ou aposentar  compulsoriamente qualquer pessoa que tivesse atentado contra a segurança do país, o regime democrático e a probidade da administração pública.<br>
    • AI-2 - instituiu a eleição indireta para presidente da República, dissolveu todos os partidos políticos existentes desde 1945, aumentou o número de ministros do Supremo Tribunal Federal de 11 para 16, reabriu o processo de punição aos adversários do regime e estabeleceu que o presidente poderia decretar estado de sítio por 180 dias sem consultar o Congresso. Também poderia o presidente intervir nos estados, decretar o recesso no Congresso, demitir funcionários por incompatibilidade com o regime e baixar decretos-lei e atos complementares sobre assuntos de segurança nacional.<br>
    • AI-3 -   estabelecia que os governadores e vices seriam eleitos indiretamente por um colégio eleitoral, formado pelos deputados estaduais. Também estabeleceu que os prefeitos das capitais seriam indicados pelos governadores, com aprovação das assembleias legislativas. Estabeleceu o calendário eleitoral, com a eleição presidencial em 3 de outubro e para o Congresso, em 15 de novembro.<br>
    • AI-4 - convocou ao Congresso Nacional o estabelecimento de uma nova carta constitucional - a Constituição de 1967,  que revogaria de forma definitiva a Constituição de 1946.<br>
    • Constituição de 1967:<br>
- Concentra no Poder Executivo a maior parte do poder de decisão<br>
- Confere somente ao Executivo o poder de legislar em matéria de segurança e orçamento;<br>
- Estabelece eleições indiretas para presidente, com mandato de cinco anos;<br>
- Tendência à centralização, embora pregue o federalismo;<br>
- Restringe ao trabalhador o direito de greve;<br>
- Ampliação da justiça Militar;<br>
- Abre espaço para a decretação posterior de leis de censura e banimento<br>
    • Programa de ação econômica do governo:<br>
- Combater a inflação (que, em 1964, era muito alta);<br>
- Aumentar os investimentos estatais (principalmente em infraestrutura);<br>
- Reformar o Sistema Financeiro Nacional;<br>
- Diminuir as desigualdades regionais (Norte-Sul);<br>
- Atrair investimentos externos.<br>
    • Em 15 de março de 1967, entrega a faixa a seu sucessor, o general Arthur da Costa e Silva<br>
<br>
    <B>• Arthur da Costa e Silva (1967-1969):</b><br>
    • eleito em 1966 como candidato único pela ARENA<br>
    • inicio do “milagre econômico<br>
    • inicio dos “anos de chumbo”<br>
    • AI-5 -  concedia ao Presidente da República, dentre outros, os poderes de cassar mandatos, intervir em estados e municípios, suspender direitos políticos de qualquer pessoa e, o mais importante, decretar recesso do Congresso e assumir suas funções legislativas no ínterim. O AI-5 também suspendeu o Habeas Corpus para crimes políticos. Por consequência, jornais oposicionistas ao regime militar foram censurados, livros e obras "subversivas" foram retiradas de circulação e vários artistas e intelectuais precisaram se exilar no estrangeiro. <br>
    • AI-6 : reduziu de 16 para 11 o número de ministros do STF, sendo aposentados compulsoriamente Antônio Carlos Lafayette de Andrada e Antônio Gonçalves de Oliveira, que haviam se manifestado contra a cassação de outros ministros do tribunal. Estabeleceu também que os crimes contra a segurança nacional seriam julgados pela justiça militar e não pelo STF.<br>
    • AI-8: estabeleceu que estados, Distrito Federal e municípios com mais de 200.000 habitantes poderiam fazer reformas administrativas por decreto.<br>
    • AI-9: Seguindo a esteira dos atos institucionais anteriores, estabeleceu regras para a reforma agrária cuja doutrinação tinha cunho estritamente conservador.
Este ato institucional dava poder ao presidente para delegar as atribuições para a desapropriação de imóveis rurais por interesse social, sendo-lhe privativa a declaração de zonas prioritárias.<br>
Costa e Silva estabeleceu também a indenização com títulos da dívida pública reembolsáveis por 20 anos, com correção monetária e que em caso de discussão do valor, seria aceito o valor cadastral da propriedade.<br>
    • Em 31 de agosto de 1969, se afasta da presidência por causa de AVC, fazendo com que uma junta governativa provisória, governasse o país até novas eleições indiretas <br>
    • Em 25 de outubro de 1969, Emilio Garrastazu Médici é escolhido pelo congresso para presidir o país.<br>
<br>
    <B>• Emilio Garrastazu Médici (1969-1974):</b><br>
    • Ápice do “milagre econômico “<br>
    • Ápice dos “anos de chumbo”<br>
    • período de mais repressão do regime <br>
    • uso extensivo do AI-5 <br>
    • AI-13: estabelecia o "banimento do território nacional de pessoas perigosas para a segurança nacional”<br>
    • AI-14: estabelecia a modificação do artigo 150 da constituição, com a aplicação da pena de morte nos casos de comprovada participação em atos de terrorismo que resultasse em morte e também em "guerra externa, revolucionária ou subversiva".<br>
    • Estatais criadas com o objetivo de desenvolvimento econômico:<br>
-Infraero<br>
-Telebrás<br>
-Valec<br>
-INCRA<br>
-Dataprev<br>
-Companhia de Desenvolvimento dos Vales do São Francisco e do Parnaíba<br>
-Telebrás<br>
-Itaipu Binacional (Hidrelétrica)<br>
-Banco Nacional da Habitação (status empresa pública)<br>
-Porto do Forno<br>
-Embraer<br>
-MOBRAL<br>
    • investimento na construção civil<br>
- ponte rio-Niterói<br> 
- hidrelétrica de Itaipu <br>

    <b>• Foi sucedido em 15 de março de 1974 por Ernesto Beckmann Geisel<br></b>
    • Ernesto Beckmann Geisel (1974-1979)<br>
    • Assume a presidência em 15 de março de 1974<br>
    • inicio da abertura politica <br>
    • Anexação da Guanabara pelo Rio de Janeiro em 1975.<br>
    • Divisão de Mato Grosso e criação de Mato Grosso do Sul entre 1977 e 1979.<br>
    • II Plano Nacional de Desenvolvimento (PND), política desenvolvimentista que ajudou a manter a economia aquecida, o parque industrial em expansão e o nível de empregos após o primeiro choque do petróleo (1973), mas que causou agravamento da dívida externa e dos problemas monetários ocasionadores da hiperinflação das décadas seguintes, legando um estado de recessão já no governo do sucessor João Figueiredo. A concessão de grandes incentivos aos investimentos de capital estrangeiro no país foi outro controverso e capital ponto do II PND. Durante seu governo houve um crescimento de 31,88% do PIB (média de 6,37%) e 19,23% da renda per capita (média de 3,84%), números porém que desaceleraram os conquistados pelo milagre econômico brasileiro(1969–1973). Geisel assumiu com uma inflação em 15,54% e deixou o cargo com 40,81%.<br>
    • Inaugurou as primeiras linhas do metrô em São Paulo e no Rio de Janeiro.<br>
    • Busca de novas fontes de energia, realizando o acordo nuclear com a Alemanha, criando os contratos de risco com a Petrobras e incentivando a utilização do álcool como combustível.<br>
    • Deu início do processo de redemocratização do país, embora tal processo tenha sofrido alguns retrocessos durante seu governo, como bem exemplifica o Pacote de Abril, em 1977.<br>
    • Abertura política do Brasil, sobre a qual Geisel afirmou que a redemocratização seria um processo "lento, gradual e seguro" em seu discurso de posse. Os assassinatos do jornalista Vladimir Herzog e do operário Manoel Fiel Filho em São Paulo, entre o final de 1975 e o início de 1976, apressaram os planos do governo e ocasionaram um enfraquecimento do poder da chamada linha-dura dentro da administração federal, com a demissão, pouco tempo depois das mortes, do general Ednardo D'Ávila Mello, comandante do II Exército, responsável pelas prisões de Herzog e Fiel Filho. No auge da crise institucional, Geisel neutraliza uma tentativa de golpe do general Sylvio Frota, principal expoente da "linha-dura" e tido como um dos favoritos à sua própria sucessão, e termina por exonerá-lo em outubro de 1977.<br>
    • Extinguiu o AI-5 e preparou o governo seguinte (João Figueiredo) para realizar a anistia política e a volta dos exilados, mas sem que retomassem seus cargos políticos.<br>
    • Em política externa, dentro de uma orientação denominada pelo governo de "pragmatismo responsável", procurou ampliar a presença brasileira na África e com os demais países do Terceiro Mundo (incluindo nisso os vizinhos de América Latina), evitando o alinhamento incondicional aos Estados Unidos. Reconheceu de imediato os regimes socialistas portugueses e angolanos que se sucederam após as guerras de independência colonial e o desmonte do poder ditatorial salazarista, bem como reatou relações diplomáticas com a República Popular da China. <br>
    • Autorizou a demolição do Palácio Monroe - episódio polêmico que movimentou interesses diversos e gerou intensas campanhas contra e a favor nos jornais. <br>
    • Denunciou o tratado militar Brasil-Estados Unidos em 1977. <br>
    • Construiu grande parte da Usina Hidrelétrica de Itaipu. <br>
    • entregou a presidência em 15 de março de 1979 a seu sucessor João Baptista de Oliveira Figueiredo, o ultimo presidente militar.<br>
<br>
    <b>• João Baptista de Oliveira Figueiredo (1979-1985)</b><br>
    • eleito Presidente da República pelo Colégio Eleitoral em 15 de outubro de 1978.<br>
    • Anistia aos punidos pelo AI-5 e perdão aos crimes de abuso de poder, tortura e assassinato cometidos por órgãos de segurança.<br>
    • Extinção do bipartidarismo, o que dividiu a oposição entre os novos partidos criados e manteve a união dos arenistas, agora no PDS, que garantiram dessa forma a presidência da Câmara.<br>
    • Garantiu o processo de abertura política, iniciado por Geisel, que resultou no fim do regime militar.<br>
    • Estabeleceu o reajuste semestral do salário.<br>
    • Criação do programa Grande Carajás.<br>
    • Criação do estado de Rondônia.<br>
    • Criação do Polonoroeste.<br>
    • Amplo programa de reforma agrária no norte do Brasil.<br>
    • Assinou a lei nº 7.116, de 23 de agosto de 1983, que assegura a validade nacional das Carteiras de Identidade.<br>
    • Assinou a atual Lei de Segurança Nacional, em 1983 <br>
    • Criação da Companhia Brasileira de Trens Urbanos (CBTU), em 1984.<br>
    • Em 15 de janeiro de 1985, Tancredo Neves é eleito presidente da republica pelo congresso.<br>
    • Em 15 de março de 1985, João Figueiredo entrega o cargo ao seu sucesso José Sarney, dando fim ao regime que já durava 21 anos.<br>
          	<br>
          	• DOI-CODI: Criado em 2 de julho de 1969, onde foi um órgão que investigava e combatia inimigos internos, que ameaçariam a segurança nacional. Um dos chefes do DOI-CODI, foi o Coronel Carlos Alberto Brilhante Ustra, chefe do órgão de São Paulo, onde prendeu cerca de 2 mil "inimigos do estado".

